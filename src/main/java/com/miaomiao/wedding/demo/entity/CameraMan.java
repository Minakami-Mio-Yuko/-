package com.miaomiao.wedding.demo.entity;

public class CameraMan {
    Integer cameramanId;
    String cameramanName;
    String cameramanStatus;

    public Integer getCameramanId() {
        return cameramanId;
    }

    public void setCameramanId(Integer cameramanId) {
        this.cameramanId = cameramanId;
    }

    public String getCameramanName() {
        return cameramanName;
    }

    public void setCameramanName(String cameramanName) {
        this.cameramanName = cameramanName;
    }

    public String getCameramanStatus() {
        return cameramanStatus;
    }

    public void setCameramanStatus(String cameramanStatus) {
        this.cameramanStatus = cameramanStatus;
    }
}
